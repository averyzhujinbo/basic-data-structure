/**
 * 二叉搜索树
 * 它和链表一样, 是动态数据结构
 * 二叉树在计算机领域中, 是一种非常常用的数据结构
 *
 * 二叉树
 * 1.二叉树具有唯一的根节点
 * 2.每个节点最多有两个孩子, 同时最多只能有一个父亲节点
 * 3.没有孩子的节点又称为叶子节点
 * 4.具有天然的递归结构
 *
 * 二分搜索树
 * 1.二分搜索树是一颗二叉树
 * 2.每个节点必须大于其左子树所有节点的值
 * 3.每个节点必须小于其右子树所有节点的值
 * 4.每一颗子树, 也是二分搜索树
 * 5.二分搜索树的中序遍历， 得到的结果是排序后的结果，二分搜索树又称之为排序树
 */
